/**
 * Convert a LandingMockProduct to StudioRenderedItem for use with OutfitInspirationTile
 */
export function toRenderedItem(product: LandingMockProduct): StudioRenderedItem {
  return {
    id: product.id,
    zone: product.type as StudioRenderedZone,
    imageUrl: product.imageUrl,
    placementX: product.placementX,
    placementY: product.placementY,
    imageLengthCm: product.imageLengthCm,
    brand: product.brand,
    productName: product.productName,
    price: product.price,
    currency: product.currency,
    gender: product.gender, // Pass gender through if needed
    bodyPartsVisible: product.bodyPartsVisible ?? null, // Pass through body parts visibility
  }
}

